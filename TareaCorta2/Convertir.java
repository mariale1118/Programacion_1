public class Convertir {

    public double metrosAKilometros(double metros) {
        return metros / 1000;
    }

    public double kilometrosAMillas(double kilometros) {
        return kilometros * 0.62137;
    }

    public double millasAKilometros(double millas) {
        return millas * 1.60934;
    }

    public double metrosAYardas(double metros) {
        return metros * 1.09361;
    }
}